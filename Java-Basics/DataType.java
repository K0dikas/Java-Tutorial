public class DataType{

	public static void main(String[] args){
	
	// How to create a variable

		int x;			// Declaration of Variable x
		x = 123;		// Assignment of Variable x
		// or
		int y = 123;	// Declaration and Initialization of Variable y 

		// Different data types in Java

		int num = 10;			// 4 bytes, only whole numbers, can't have decimals
		float decimal = 1.50f;		// 4 bytes, only numbers with decimals, can't have whole numbers
		double decimals = 25.00;	// 8 bytes, only number with decimals, can't have whole numbers

		boolean crap = false;		// 1 bit, value could be true or false
		boolean truth = true;		// 1 bit, value could be true or false

		char letter = 'A';		      // 2 bytes, can only have single character or symbols
		String word = "Mark D.Cruz have a crush"; // Size varies, group of characters

		long z = 12345678910L;		//  8 bytes, a bunch of numbers
	}
}
